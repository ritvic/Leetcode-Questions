class Solution
{
public:
    vector<int> candyStore(int candies[], int N, int K)
    {
        // Write Your Code here
        int x = (ceil)((double)N/(1+K));
        sort(candies, candies+N);
        int a = accumulate(candies, candies+x, 0);
        int b = accumulate(candies+N-x, candies+N, 0);
        return {a, b};
    }
};

