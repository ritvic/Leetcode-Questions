class Solution{   
public:
    int median(vector<vector<int>> &v, int m, int n){
        // code here       
        int k=(n*m+1)/2,ans;
        sort(v.begin(),v.end());
        priority_queue<vector<int>, vector<vector<int>>, greater<>> minHeap;
        for (int r = 0; r < min(m, k); ++r)
            minHeap.push({v[r][0], r, 0});

        for (int i = 1; i <= k; ++i) {
            auto top = minHeap.top(); minHeap.pop();
            int r = top[1], c = top[2];
            ans = top[0];
            if (c + 1 < n) minHeap.push({v[r][c + 1], r, c + 1});
        }
        return ans;
    }
    
};

